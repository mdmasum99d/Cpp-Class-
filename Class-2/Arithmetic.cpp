#include<iostream>
using namespace std;
int main()
{
    int a=7;
    cout << "a = " <<7 <<endl;
    cout << "a+ = 5 is " << (a+=5) <<endl;
    cout << "a- = 2 is " << (a-=2) << endl;
    cout << "a* = 3 is " << (a*=3) << endl;
    cout << "a/ = 4 is " << (a/=4) << endl;
    cout << "a% = 2 is " << (a%=2) << endl;
    return 0;
}
