#include<iostream>
using namespace std;
int main()
{
    int a=5,b=3;
    cout << "a == b is " << (a==b) << endl;
    cout << "a != b is " << (a!=b) << endl;
    cout << "a > b is " << (a>b) << endl;
    cout << "a < b is " << (a<b) << endl;
    cout << "a >= b is " << (a>=b) << endl;
    cout << "a <= b is " << (a<=b) << endl;
    return 0;


}
